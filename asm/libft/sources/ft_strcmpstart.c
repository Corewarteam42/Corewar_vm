/*
** Renvoie 1 si s2 correspon u debut de s1
*/

int		ft_strchrstart(const char *s1, const char *s2)
{
	while (*s2 && *s1)
	{
		if (*s2 != *s1)
			return (0);
		++s1;
		++s2;
	}
	if (!*s2)
		return (1);
	return (0);
}
